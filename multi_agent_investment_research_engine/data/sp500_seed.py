def write_csv(out_path: Path) -> int:
    """Write the seed list to `out_path`. Returns the number of rows."""
    out_path.parent.mkdir(parents=True, exist_ok=True)

    # Dedupe by ticker - the curated list above can have a few stragglers
    # from copy/paste; we keep the first occurrence per symbol.
    seen: set[str] = set()
    rows: list[dict[str, str]] = []
    for ticker, name, sector, sub_industry in CONSTITUENTS:
        if ticker in seen:
            continue
        seen.add(ticker)
        rows.append(
            {
                "ticker": ticker,
                "company_name": name,
                "sector": sector,
                "sub_industry": sub_industry,
            }
        )
    df = pd.DataFrame(rows).sort_values(["sector", "ticker"]).reset_index(drop=True)
    df.to_csv(out_path, index=False)
    return len(df)


